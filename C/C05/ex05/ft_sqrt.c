#include <unistd.h>

int	ft_sqrt(int nb)
{
	int	e;

	e = 0;
	if (nb <= 0)
	{
		return (0);
	}
	if (nb == 1)
	{
		return (1);
	}
	while (e * e <= nb && e <= 46340)
	{
		if ((e * e) == nb)
		{
			return (e);
		}
		e++;
	}
	return (0);
}
